package loop_study;

public class forloop_natural_no {

	public static void main(String[] args) {
		
		for(int i=1;i<=10;i++)
		{
		System.out.println(i );	
		}
		System.out.println("================");
		for(int i=15;i>=1;i--)
		{
			System.out.println(i );	
		}
		System.out.println("----------------");
		for(int i=1;i<=10;i++)
		{
			int square = i*i;
		System.out.println(square );	
		}
		System.out.println("----------------");
		for(int j=1; j<=10;j++)
		{
			int cube = j*j*j;
			System.out.println(cube);
		}

	}

}
